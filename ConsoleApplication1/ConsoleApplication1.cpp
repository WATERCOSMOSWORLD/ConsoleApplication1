#include <iostream>
#include <locale> // Необходима для работы с русским языком

int main() {
    // Устанавливаем русскую локаль для консоли
    setlocale(LC_ALL, "Russian");

    double число1, число2;
    char операция;

    std::cout << "Введите первое число: ";
    std::cin >> число1;

    std::cout << "Введите операцию (+, -, *, /): ";
    std::cin >> операция; // Считываем символ операции

    std::cout << "Введите второе число: ";
    std::cin >> число2;

    double результат;

    // Используем конструкцию switch, она отлично подходит для выбора из нескольких вариантов
    switch (операция) {
    case '+':
        результат = число1 + число2;
        std::cout << "Сумма: " << результат << std::endl;
        break;
    case '-':
        результат = число1 - число2;
        std::cout << "Разность: " << результат << std::endl;
        break;
    case '*':
        результат = число1 * число2;
        std::cout << "Произведение: " << результат << std::endl;
        break;
    case '/':
        // Важная проверка: нельзя делить на ноль
        if (число2 != 0) {
            результат = число1 / число2;
            std::cout << "Частное: " << результат << std::endl;
        }
        else {
            std::cout << "Ошибка! Деление на ноль невозможно." << std::endl;
        }
        break;
    default:
        // Если пользователь ввел что-то другое
        std::cout << "Ошибка! Неверная операция." << std::endl;
        break;
    }

    // Добавляем паузу, чтобы консоль не закрывалась сразу
    std::cout << "Нажмите любую клавишу, чтобы завершить...";
    std::cin.ignore(); // Игнорируем предыдущий ввод
    std::cin.get();    // Ожидаем нажатия клавиши

    return 0;
}